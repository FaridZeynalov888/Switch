let a = prompt("Enter the month:")

switch(a){
    case "January":
        alert("First month - Winter seasons")
        break;
    case "February":
        alert("Second month - Winter seasons")
        break;
    case "March":
        alert("Third month - Spring seasons")
        break
    case "April":
        alert("Fourth month - Spring seasons")
        break;
    case "May":
        alert("Fifth month - Spring seasons")
        break;
    case "June":
        alert("Sixth month - Summer seasons")
        break;
    case "July":
        alert("Seventh month - Summer seasons")
        break;
    case "August":
        alert("Eighth month - Summer seasons")
        break;
    case "September":
        alert("Ninth month - Autumn seasons")
        break;
    case "October":
        alert("Tenth month - Autumn seasons")
        break;
    case "November":
        alert("Eleventh month - Autumn seasons")
        break;
    case "December":
        alert("Twelfth month - Winter seasons")
        break;
        default:
            alert("There is no such month");
}

